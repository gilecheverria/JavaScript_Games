/*
 * Class for a rectangle, described as top left coordinate position,
 * width and height.
 * Used for sprites.
 *
 * Gilberto Echeverria
 * 2026-02-10
 */

"use strict";

class Rect {
    constructor(x, y, width, height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
